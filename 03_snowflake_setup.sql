USE SCHEMA ANALYTICS;

-- DIMENSION: Hospital
CREATE OR REPLACE TABLE ANALYTICS.DIM_HOSPITAL (
    HOSPITAL_SK     NUMBER AUTOINCREMENT PRIMARY KEY,  -- surrogate key
    HOSPITAL_ID     NUMBER NOT NULL,                    -- natural key
    HOSPITAL_NAME   VARCHAR(100),
    CITY            VARCHAR(50),
    STATE           VARCHAR(50),
    TIER            VARCHAR(10),
    TOTAL_BEDS      NUMBER,
    ICU_BEDS        NUMBER,
    VENTILATORS     NUMBER,
    LAT             FLOAT,
    LON             FLOAT,
    EFF_FROM        DATE DEFAULT CURRENT_DATE(),
    EFF_TO          DATE DEFAULT '9999-12-31',
    IS_CURRENT      BOOLEAN DEFAULT TRUE
);

-- DIMENSION: Department
CREATE OR REPLACE TABLE ANALYTICS.DIM_DEPARTMENT (
    DEPARTMENT_SK   NUMBER AUTOINCREMENT PRIMARY KEY,
    DEPARTMENT_ID   NUMBER NOT NULL,
    HOSPITAL_ID     NUMBER,
    DEPARTMENT_NAME VARCHAR(50),
    TOTAL_BEDS      NUMBER,
    FLOOR_NUMBER    NUMBER
);

-- DIMENSION: Ambulance
CREATE OR REPLACE TABLE ANALYTICS.DIM_AMBULANCE (
    AMBULANCE_SK        NUMBER AUTOINCREMENT PRIMARY KEY,
    AMBULANCE_ID        NUMBER NOT NULL,
    REGISTRATION_NO     VARCHAR(20),
    HOSPITAL_ID         NUMBER,
    VEHICLE_TYPE        VARCHAR(20),
    MANUFACTURE_YEAR    NUMBER,
    STATUS              VARCHAR(20)
);

-- DIMENSION: Date (pre-built calendar)
CREATE OR REPLACE TABLE ANALYTICS.DIM_DATE (
    DATE_ID         NUMBER PRIMARY KEY,
    FULL_DATE       DATE,
    DAY_OF_WEEK     VARCHAR(10),
    DAY_NUM         NUMBER,
    WEEK_NUM        NUMBER,
    MONTH_NUM       NUMBER,
    MONTH_NAME      VARCHAR(15),
    QUARTER         NUMBER,
    YEAR            NUMBER,
    IS_WEEKEND      BOOLEAN,
    IS_HOLIDAY      BOOLEAN
);

-- FACT: Bed Occupancy
CREATE OR REPLACE TABLE ANALYTICS.FACT_BED_OCCUPANCY (
    OCCUPANCY_SK        NUMBER AUTOINCREMENT PRIMARY KEY,
