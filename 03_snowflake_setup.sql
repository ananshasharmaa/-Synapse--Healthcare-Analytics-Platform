-- View: Hospital capacity dashboard
CREATE OR REPLACE VIEW ANALYTICS.VW_HOSPITAL_CAPACITY AS
SELECT
    dh.HOSPITAL_NAME,
    dh.TIER,
    dd.DEPARTMENT_NAME,
    dt.FULL_DATE,
    dt.MONTH_NAME,
    dt.QUARTER,
    dt.YEAR,
    fo.SNAPSHOT_HOUR,
    fo.TOTAL_BEDS,
    fo.OCCUPIED_BEDS,
    fo.AVAILABLE_BEDS,
    fo.OCCUPANCY_PCT,
    fo.ON_HOLD_BEDS,
    fo.CRITICAL_FLAG,
    -- 7-day rolling average occupancy per department
    AVG(fo.OCCUPANCY_PCT) OVER (
        PARTITION BY fo.DEPARTMENT_SK
        ORDER BY dt.FULL_DATE
        ROWS BETWEEN 6 PRECEDING AND CURRENT ROW
    ) AS rolling_7d_avg_occ
FROM ANALYTICS.FACT_BED_OCCUPANCY fo
JOIN ANALYTICS.DIM_HOSPITAL   dh ON fo.HOSPITAL_SK   = dh.HOSPITAL_SK
JOIN ANALYTICS.DIM_DEPARTMENT dd ON fo.DEPARTMENT_SK = dd.DEPARTMENT_SK
JOIN ANALYTICS.DIM_DATE       dt ON fo.DATE_ID       = dt.DATE_ID;

-- View: Ambulance operations
CREATE OR REPLACE VIEW ANALYTICS.VW_AMBULANCE_OPS AS
SELECT
    dh.HOSPITAL_NAME,
    da.VEHICLE_TYPE,
    da.REGISTRATION_NO,
    dt.FULL_DATE,
    dt.MONTH_NAME,
    dt.DAY_OF_WEEK,
    dt.IS_WEEKEND,
    fad.CALL_TYPE,
    fad.CALL_OUTCOME,
    fad.RESPONSE_TIME_MIN,
    fad.ON_SCENE_TIME_MIN,
    fad.TRANSPORT_TIME_MIN,
    fad.TOTAL_TIME_MIN,
    fad.SLA_BREACHED,
    fad.CREW_COUNT,
    -- Percentile ranking of response times
    PERCENT_RANK() OVER (
        PARTITION BY dh.HOSPITAL_SK, fad.CALL_TYPE
        ORDER BY fad.RESPONSE_TIME_MIN
    ) AS response_time_pctile
FROM ANALYTICS.FACT_AMBULANCE_DISPATCH fad
JOIN ANALYTICS.DIM_HOSPITAL   dh ON fad.HOSPITAL_SK  = dh.HOSPITAL_SK
JOIN ANALYTICS.DIM_AMBULANCE  da ON fad.AMBULANCE_SK = da.AMBULANCE_SK
JOIN ANALYTICS.DIM_DATE       dt ON fad.DATE_ID      = dt.DATE_ID;

-- View: Patient flow KPIs
CREATE OR REPLACE VIEW ANALYTICS.VW_PATIENT_FLOW AS
SELECT
    dh.HOSPITAL_NAME,
    dd.DEPARTMENT_NAME,
    dt.FULL_DATE,
    dt.MONTH_NAME,
    dt.QUARTER,
    pf.CONDITION,
    pf.ENTRY_MODE,
    pf.AGE_GROUP,
    pf.GENDER,
    pf.LENGTH_OF_STAY_HRS,
    pf.ER_TO_WARD_HRS,
    pf.READMISSION_FLAG,
    pf.DISCHARGE_TYPE,
    -- Average LOS by condition (window function)
    AVG(pf.LENGTH_OF_STAY_HRS) OVER (
        PARTITION BY pf.CONDITION
    ) AS avg_los_by_condition,
    -- Running admission count per hospital per month
    COUNT(*) OVER (
        PARTITION BY dh.HOSPITAL_SK, dt.MONTH_NUM, dt.YEAR
        ORDER BY pf.ADMIT_TIMESTAMP
    ) AS running_monthly_admissions
FROM ANALYTICS.FACT_PATIENT_FLOW pf
JOIN ANALYTICS.DIM_HOSPITAL   dh ON pf.HOSPITAL_SK   = dh.HOSPITAL_SK
JOIN ANALYTICS.DIM_DEPARTMENT dd ON pf.DEPARTMENT_SK = dd.DEPARTMENT_SK
JOIN ANALYTICS.DIM_DATE       dt ON pf.DATE_ID       = dt.DATE_ID;
